print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")

direction = input("Will you go left or right?\n")
if direction.lower() != "left":
    print("You fell in a hole and died. Game over")
else:
    direction = input("You come across a body of water. Will you swim or wait?\n")
    if direction.lower() != "wait":
        print("You were attacked by a trout and died. Game over")
    else:
        direction = input("You come across three doors. Will you go through "
                          "the red door, the blue door, or the yellow door?\n")
        if direction.lower() == "yellow":
            print("You win!")
        elif direction.lower() == "red":
            print("Burned by fire. Game over.")
        elif direction.lower() == "blue":
            print("Eaten by beasts. Game over")
        else:
            print("Game over.")

